    private void cargarCombos() {
    cboUsos.removeAllItems();
    cboUsos.addItem("Facial");
    cboUsos.addItem("Corporal");
    cboUsos.addItem("Relajacion");
    cboUsos.addItem("Estetico");

    cboApto.removeAllItems();
    cboApto.addItem("Si");
    cboApto.addItem("No");
}

private void prepararTabla() {
    modelo = new DefaultTableModel(new Object[]{"Nro","Usos","Apto","Equipamiento"}, 0){
        @Override public boolean isCellEditable(int r,int c){ return false; }
    };
    tblConsultorios.setModel(modelo);
    tblConsultorios.setRowHeight(22);
    
}

private void cargarTabla() {
    modelo.setRowCount(0);
    List<Consultorio> lista = consultorioData.listarConsultorios();
    for (Consultorio c : lista) {
        modelo.addRow(new Object[]{
            c.getNroConsultorio(), c.getUsos(), c.getApto(), c.getEquipamiento()
        });
    }
}

private void limpiarCampos() {
    txtNro.setText("");
    cboUsos.setSelectedIndex(0);
    cboApto.setSelectedIndex(0);
    txtEquip.setText("");
    txtBuscar.setText("");
    tblConsultorios.clearSelection();
}

private Consultorio leerFormulario() {
    Consultorio c = new Consultorio();
    if (!txtNro.getText().isBlank()) {
        c.setNroConsultorio(Integer.parseInt(txtNro.getText().trim()));
    }
    c.setUsos((String)cboUsos.getSelectedItem());
    c.setApto((String)cboApto.getSelectedItem());
    c.setEquipamiento(txtEquip.getText().trim());
    return c;
}

private void pasarFilaAFormulario() {
    int row = tblConsultorios.getSelectedRow();
    if (row >= 0) {
        txtNro.setText(String.valueOf(modelo.getValueAt(row, 0)));
        cboUsos.setSelectedItem(modelo.getValueAt(row, 1));
        cboApto.setSelectedItem(modelo.getValueAt(row, 2));
        txtEquip.setText(String.valueOf(modelo.getValueAt(row, 3)));
    }
}


