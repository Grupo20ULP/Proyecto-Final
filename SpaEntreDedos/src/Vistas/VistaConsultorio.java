    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
        Consultorio c = leerFormulario();
        if (c.getUsos()==null || c.getApto()==null) {
            JOptionPane.showMessageDialog(this, "Completa Usos y Apto.");
            return;
        }
        consultorioData.guardarConsultorio(c);
        cargarTabla();
        if (c.getNroConsultorio() > 0) {
            txtNro.setText(String.valueOf(c.getNroConsultorio()));
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error al guardar: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        if (txtNro.getText().isBlank()) {
        JOptionPane.showMessageDialog(this, "Selecciona un registro de la tabla.");
        return;
    }
    Consultorio c = leerFormulario();
    consultorioData.actualizarConsultorio(c);
    cargarTabla();
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (txtNro.getText().isBlank()) {
        JOptionPane.showMessageDialog(this, "Selecciona un registro.");
        return;
    }
    int nro = Integer.parseInt(txtNro.getText());
    int ok = JOptionPane.showConfirmDialog(this,
        "¿Eliminar consultorio Nº " + nro + "?", "Confirmacion",
        JOptionPane.YES_NO_OPTION);
    if (ok == JOptionPane.YES_OPTION) {
        consultorioData.eliminarConsultorio(nro);
        cargarTabla();
        limpiarCampos();
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String q = txtBuscar.getText().trim();
    if (q.isBlank()) { cargarTabla(); return; }

    try {
        int nro = Integer.parseInt(q);
        var c = consultorioData.buscarConsultorio(nro);
        modelo.setRowCount(0);
        if (c != null) {
            modelo.addRow(new Object[]{
                c.getNroConsultorio(), c.getUsos(), c.getApto(), c.getEquipamiento()
            });
        } else {
            JOptionPane.showMessageDialog(this, "No encontrado.");
        }
    } catch (NumberFormatException e) {
        // Filtro por 'usos'
        cargarTabla();
        for (int i = modelo.getRowCount() - 1; i >= 0; i--) {
            String usos = String.valueOf(modelo.getValueAt(i, 1));
            if (!usos.toLowerCase().contains(q.toLowerCase())) {
                modelo.removeRow(i);
            }
        }
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

