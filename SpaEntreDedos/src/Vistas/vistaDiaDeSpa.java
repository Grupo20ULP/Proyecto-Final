    private void jTFCodPackMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTFCodPackMouseClicked
        try {
            Dia_De_SpaData diaData = new Dia_De_SpaData();

            // Convertir el texto a entero
            int codPack;
            try {
                codPack = Integer.parseInt(jTFCodPack.getText());
            } catch (NumberFormatException e) {
                jTFCodPack.setText("");
                return; // si no es un número válido, salimos
            }

            Modelo.Dia_De_Spa dia = diaData.buscarDiaDeSpa(codPack);

            if (dia != null) {
                jTFCodPack.setText(String.valueOf(dia.getCodPack()));
                jTFFecha.setText(dia.getFechaHora().toString());
                jTXTAREAPREF.setText(dia.getPreferencias());
                jTFTotal.setText(String.valueOf(dia.getMonto()));
                jCBCliente.setSelectedItem(dia.getCliente().getNombre_completo());
            } else {
                // Si no encuentra nada, limpiamos campos
                jTFFecha.setText("");
                jTXTAREAPREF.setText("");
                jTFTotal.setText("");
                jCBCliente.setSelectedIndex(-1);
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jTFCodPackMouseClicked

    private void BtnBuscarCodPackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarCodPackActionPerformed
        try {
            int codPack = Integer.parseInt(jTFCodPack.getText().trim());
            Dia_De_SpaData diaData = new Dia_De_SpaData();
            Modelo.Dia_De_Spa dia = diaData.buscarDiaDeSpa(codPack);

            if (dia != null) {
                jTFCodPack.setText(String.valueOf(dia.getCodPack()));
                jTFFecha.setText(dia.getFechaHora().toString());
                jTXTAREAPREF.setText(dia.getPreferencias());
                jTFTotal.setText(String.valueOf(dia.getMonto()));

                // Seleccionar cliente en el combo
                String itemCliente = dia.getCliente().getCodCli()
                        + " - " + dia.getCliente().getNombre_completo();
                jCBCliente.setSelectedItem(itemCliente);


                // Seleccionar estado
                jCBEstado.setSelectedItem(dia.getEstado());

                // Cargar sesiones en la tabla
                mostrarSesiones(dia.getSesiones());
                //️ AQUI se actualiza la tabla principal con SOLO este día
                mostrarSoloEsteDia(dia);

            } else {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "No se encontró el Día de Spa con ese código.");
                limpiarCampos();
            }

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Ingrese un número válido en CodPack.");
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_BtnBuscarCodPackActionPerformed

    private void jBtnListarDiasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnListarDiasActionPerformed
        try {
            Dia_De_SpaData diaData = new Dia_De_SpaData();
            List<Modelo.Dia_De_Spa> dias = diaData.listarDiasDeSpa();

            // Crear modelo para la tabla
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("CodPack");
            modelo.addColumn("Fecha");
            modelo.addColumn("Cliente");
            modelo.addColumn("Monto");
            modelo.addColumn("Estado");

            for (Modelo.Dia_De_Spa dia : dias) {
                Object[] fila = {
                    dia.getCodPack(),
                    dia.getFechaHora().toString(),
                    dia.getCliente().getNombre_completo(),
                    dia.getMonto(),
                    dia.getEstado()
                };
                modelo.addRow(fila);
            }

            jTableSesiones.setModel(modelo);

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al listar los días de spa.");
        }
    }//GEN-LAST:event_jBtnListarDiasActionPerformed

    private void jBtnEliminarDiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEliminarDiaActionPerformed
        try {
            int codPack = Integer.parseInt(jTFCodPack.getText().trim());

            // Confirmación antes de eliminar
            int respuesta = JOptionPane.showConfirmDialog(this,
                    "¿ESTÁS SEGURO QUE QUIERES ELIMINAR este Día de Spa y todas sus sesiones?",
                    "Confirmar eliminación",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.WARNING_MESSAGE);

            if (respuesta == JOptionPane.YES_OPTION) {
                Dia_De_SpaData diaData = new Dia_De_SpaData();

                // Primero eliminamos las sesiones vinculadas al día
                boolean sesionesEliminadas = diaData.eliminarSesionesPorDia(codPack);

                // Después eliminamos el día de spa
                boolean eliminado = diaData.eliminarDiaDeSpa(codPack);

                if (eliminado) {
                    JOptionPane.showMessageDialog(this, "Día de Spa y sus sesiones eliminados correctamente.");
                    limpiarCampos();
                } else {
                    JOptionPane.showMessageDialog(this, "No se encontró el Día de Spa a eliminar.");
                }
            } // Si el usuario elige NO, no hace nada

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese un número válido en CodPack.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al eliminar: " + ex.getMessage());
        }
    }//GEN-LAST:event_jBtnEliminarDiaActionPerformed

